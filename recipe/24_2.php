	<h3>習俗</h3>
<p>民間到了雨水節，出嫁的女兒紛紛帶上禮物回娘家拜望父母。生育了孩子的婦女，須帶上罐罐肉、椅子等禮物，感謝父母的養育之恩。久不懷孕的婦女，則由母親為其縫製一條紅褲子，穿到貼身處，據說，這樣可使其儘快懷孕生子。此項風俗現仍在農村流行。<br />
雨水這天在民間還有一項特具風趣的活動叫&ldquo;拉保保&rdquo;。(保保則是乾爹）。而雨水節拉乾爹，意取&ldquo;雨露滋潤易生長&rdquo;之意。<br />
以前人們都有一個為自己兒女求神問卦的習慣，看看自己兒女命相如何，需不需要找個乾爹。而找乾爹的目的，則是為了讓兒子或女兒順利，健康的成長。於是便有了雨水節拉保保的活動。此舉一年復一年，久而成為一方之俗。</p>
