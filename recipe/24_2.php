	<h3>由來</h3>
<p>雨水是24節氣中的第2個節氣。每年的2月19日前後，太陽黃經達330度時，是二十四節氣的雨水。此時，氣溫回升、冰雪融化、降水增多，故取名為雨水。雨水節氣一般從2月18日或19日開始，到3月4日或5日結束。雨水和穀雨、小雪、大雪一樣，都是反映降水現象的節氣。<br />
《月令七十二候集解》：&ldquo;正月中，天一生水。春始屬木，然生木者 雨水必水也，故立春後繼之雨水。且東風既解凍，則散而為雨矣。&rdquo;意思是說，雨水節氣前後，萬物開始萌動，春天就要到了。如在《逸周書》中就有雨水節後&ldquo;鴻雁來&rdquo;&ldquo;草木萌動&rdquo;等物候記載。</p>
