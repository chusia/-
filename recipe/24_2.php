	<h3>養生</h3>
<p>在雨水節氣之後，隨著降雨有所增多，寒濕之邪最易困著脾臟。同時濕邪留戀，難以去除，故雨水前後應當著重養護脾臟。雨水節氣中，地濕之氣漸升，且早晨時有露、霜出現。所以針對這樣的氣候特點，飲食調養應側重於調養脾胃和祛風除濕。又由於此時氣候較陰冷，可以適當地進補，如蜂蜜、大棗、山藥、銀耳等都是適合這一節氣的補品。<br />
這種變化無常的天氣，容易引起人的情緒波動，乃至心神不安，影響人的身心健康，對高血壓、心臟病、哮喘患者更是不利。為了消除這些不利的因素，除了應當繼續進行春捂外，應採取積極的精神調攝養生鍛煉法。保持情緒穩定對身心健康有著十分重要的作用。<br />
雨水後，春風送暖，致病的細菌、病毒易隨風傳播，故春季傳染病常易暴發流行。每個人應該保護好自己，注意鍛煉身體，增強抵抗力，預防疾病的發生。</p>
