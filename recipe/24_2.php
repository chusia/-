	<h3>介紹</h3>
<p>西曆每年二月十八日前後為雨水節氣，此時太陽到達黃經330度，為交&ldquo;雨水&rdquo;節氣。雨水，表示兩層意思，一是天氣回暖，降水量逐漸增多了，二是在降水形式上，雪漸少了，雨漸多了，《月令七十二候集解》中說：&ldquo;正月中，天一生水。春始屬木，然生木者必水也，故立春後繼之雨水。且東風既解凍，則散而為雨矣。&rdquo; <br />
我國古代將雨水分為三候：&ldquo;一候獺祭魚；二候鴻雁來；三候草木萌勸。&rdquo;此節氣，水獺開始捕魚了，將魚擺在岸邊如同先祭後食的樣子；五天過後，大雁開始從南方飛回北方；再過五天，在&ldquo;潤物細無聲&rdquo;的春雨中，草木隨地中陽氣的上騰而開始抽出嫩芽。從此，大地漸漸開始呈現出一派欣欣向榮的景象。<br />
雨水節氣一般從2月18日或19日開始，到3月4日或5日結束。</p>
	
