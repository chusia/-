	<h3>養生</h3>
<p>在中醫養生理論中，夏至是陽氣最旺的時節，這一時節的養生保健，一方面要順應夏季陽盛於外的特點，注意保護陽氣；另一方面，夏至也是所謂&ldquo;陰陽爭死生分&rdquo;的時節，俗話說&ldquo;夏至&mdash;陰生&rdquo;，也就是說，儘管天氣炎熱，可陰氣已開始生長。 <br />
在此時節，為順應自然界陰陽盛衰的變化，一般宜晚睡早起，並利用午休來彌補夜晚睡眠的不足。年老體弱者則應早睡早起，儘量保持每天有7小時的睡眠時間。夏至時節，大多數人會有全身困倦乏力以及頭痛頭暈的症狀，嚴重者可影響日常生活和工作。究其原因，首先是由於這一時節氣溫高，人體只能通過排汗來散熱，使人體內的水分大量流失，此時若不及時補充水分，就會使人體血容量減少，大腦會因此而供血不足，進而造成頭痛頭暈。造成頭痛頭暈的另一種原因是，人體出汗時體表血管會擴張，更多的血液會流向體表，這種血液的再分配可使血壓偏低的人血壓更低，從而發生頭痛頭暈。</p>
