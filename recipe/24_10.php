	<h3>由來</h3>
<p>夏至，古時又稱&ldquo;夏節&rdquo;、&ldquo;夏至節&rdquo;。古時夏至日，人們通過祭神以祈求災消年豐。《周禮&middot;春官》載：&ldquo;以夏日至，致地方物魈。&rdquo;周代夏至祭神，意為清除疫癘、荒年與饑餓死亡。<br />
《史記&middot;封禪書》記載：&ldquo;夏至日，祭地，皆用樂舞。&rdquo;夏至作為古代節日，宋朝在夏至之日始，百官放假三天，遼代則是&ldquo;夏至日謂之&lsquo;朝節&rsquo;，婦女進彩扇，以粉脂囊相贈遺&rdquo;（《遼史》），清朝又是&ldquo;夏至日為交時，日頭時、二時、末時，謂之&lsquo;三時&rsquo;，居人慎起居、禁詛咒、戒剃頭，多所忌諱&hellip;&hellip;&rdquo;（《清嘉錄》）。</p>

