<h3>習俗</h3>
<p>夏至這天山東各地普遍要吃涼麵條，俗稱過水面，有&ldquo;冬至餃子夏至面&rdquo;的諺語。煙臺萊陽一帶夏至日薦新麥，黃縣(今煙臺龍口市)一帶則煮新麥粒吃，兒童們用麥秸編一個精緻的小笊籬，在湯水中一次一次地向嘴裡撈，既吃了麥粒，又是一種遊戲，很有農家生活的情趣。<br />
夏至後第三個庚日為初伏，第四庚日為中伏，立秋後第一個庚日為末伏，總稱伏日。伏日人們食欲不振，往往比常日消瘦，俗謂之&ldquo;苦夏&rdquo;，山東有的地方吃生黃瓜和煮雞蛋來治&ldquo;苦夏&rdquo;，入伏的早晨吃雞蛋，不吃別的食物。<br />
廣東地區，人們喜好在夏天滋補涼食避暑，因此居民在夏季普遍煲清補涼湯、涼茶、酸梅湯等比較傳統而且十分清甜的食物。而且夏天食物多調醋，少鹽、少薑、少蒜、味清淡，以粉面、瘦肉、青菜、瓜類等為主。</p>

