	<h3>介紹</h3>
<p>每年的6月21日或22日，為夏至日。夏至這天，太陽直射地面的位置到達一年的最北端，幾乎直射北回歸線(北緯23&deg;26&prime;28&Prime;44)，北半球的白晝達最長，且越往北越長。<br />
夏至是北半球一年中白晝最長的一天，南方各地從日出到日落大多為十四小時左右。夏至這天雖然白晝最長，太陽角度最高，但並不是一年中天氣最熱的時候。因為，接近地表的熱量，這時還在繼續積蓄，並沒有達到最多的時候。<br />
俗話說&ldquo;熱在三伏&rdquo;，真正的暑熱天氣是以夏至和立秋為基點計算的。大約在七月中旬到八月中旬，我國各地的氣溫均為最高，有些地區的最高氣溫可達40℃左右。</p>
