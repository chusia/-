	<h3>時間</h3>
<p>每年06月21~22日</p>
<h3>三候</h3>
<p>鹿角解：夏至日陰氣生而陽氣始衰，所以陽性的鹿角便開始脫落。<br />
蜩始鳴：雄性的蟬在夏至後因感陰氣之生便鼓翼而鳴。<br />
半夏生：半夏是一種喜陰的藥草，因在仲夏的沼澤地或水田中出生所以得名。</p>
