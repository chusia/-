<h3>習俗</h3>
<p>南京：吃菜飯<br />
古時，南京人對小寒頗重視，但隨著時代變遷，現已漸漸淡化，如今人們只能從生活中尋找出點點痕跡。<br />
到了小寒，老南京一般會煮菜飯吃，菜飯的內容並不相同，有用矮腳黃青菜與鹹肉片、香腸片或是板鴨丁，再剁上一些生薑粒與糯米一起煮的，十分香鮮可口。其中矮腳黃、香腸、板鴨都是南京的著名特產，可謂是真正的&ldquo;南京菜飯&rdquo;，甚至可與臘八粥相媲美。<br />
廣東：吃糯米飯<br />
廣州傳統，小寒早上吃糯米飯，為避免太糯，一般是60%糯米40%香米，把臘肉和臘腸切碎，炒熟，花生米炒熟，加一些碎蔥白，拌在飯裡面吃。<br />
中醫認為寒為陰邪，最寒冷的節氣也是陰邪最盛的時期，從飲食養生的角度講，要特別注意在日常飲食中多食用一些溫熱食物以補益身體，防禦寒冷氣候對人體的侵襲。日常食物中屬於熱性的食物主要有鱒魚、辣椒、肉桂、花椒等；屬於溫性的食物有糯米、高粱米、刀豆、韭菜、茴香、香菜、薺菜、蘆筍、芥菜、南瓜、生薑、蔥、大蒜、杏子、桃子、大棗、桂圓、荔枝、木瓜、櫻桃、石榴、烏梅、香椽、佛手、栗子、核桃仁、杏仁、羊肉、豬肝、豬肚、火腿、狗肉、雞肉、羊乳、鵝蛋、鱔魚、鱅魚、鰱魚、蝦、海參、淡菜、蚶、酒等。特別要提出的是，小寒時節正是吃麻辣火鍋、紅燜羊肉的好時節。</p>
