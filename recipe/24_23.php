	<h3>由來</h3>
<p>每年1月5日或6日太陽到達黃經285&deg;時為小寒（slight cold），它與大寒、小暑、大暑及處暑一樣，都是表示氣溫冷暖變化的節氣。《月令七十二候集解》：&ldquo;十二月節，月初寒尚小，故雲。月半則大矣。&rdquo;小寒的意思是天氣已經很冷，我國大部分地區小寒和大寒期間一般都是最冷的時期，&ldquo;小寒&rdquo;一過，就進入&ldquo;出門冰上走&rdquo;的三九天了。<br />
<br />
我國古代將小寒分為三候：&ldquo;一候雁北鄉，二候鵲始巢，三候雉始鴝&rdquo;，古人認為候鳥中大雁是順陰陽而遷移，此時陽氣已動，所以大雁開始向北遷移；此時北方到處可見到喜鵲，並且感覺到陽氣而開始築巢；第三候&ldquo;雉鴝&rdquo;的&ldquo;鴝&rdquo;為鳴叫的意思，雉在接近四九時會感陽氣的生長而鳴叫。</p>
