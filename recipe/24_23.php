<h3>介紹</h3>
<p>小寒是第二十三個節氣，在1月5-7日之間，太陽位於黃經285&deg;。對於中國而言，小寒標誌著開始進入一年中最寒冷的日子。根據中國的氣象資料，小寒是氣溫是最低的的節氣，只有少數年份的大寒氣溫低於小寒的。同時，有同名作詞人小寒。</p>
