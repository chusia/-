	<h3>養生</h3>
<p>立秋的氣候是由熱轉涼的交接節氣，也是陽氣漸收，陰氣漸長，由陽盛逐漸轉變為陰盛的時期，是萬物成熟收穫的季節，也是人體陰陽代謝出現陽消陰長的過渡時期。因此秋季養生，凡精神情志、飲食起居、運動鍛煉、皆以養收為原則。<br />
貼秋膘，民間流行在立秋這天以懸秤稱人，將體重與立夏時對比。因為人到夏天，本就沒有什麼胃口，飯食清淡簡單，兩三個月下來，體重大都要減少一點。秋風一起，胃口大開，想吃點好的，增加一點營養，補償夏天的損失，補的辦法就是&ldquo;貼秋膘&rdquo;：在立秋這天各種各樣的肉，燉肉烤肉紅燒肉等等，&ldquo;以肉貼膘&rdquo;。<br />
秋內應於肺，肺在志為悲（憂），悲憂易傷肺，肺氣虛則機體對不良刺激的耐受性下降，易生悲憂之情緒，所以在進行自我調養時切不可背離自然規律，循其古人之綱要&ldquo;使志安寧，以緩秋刑，收斂神氣，使秋氣平；無外其志，使肺氣清，此秋氣之應，養收之道也&rdquo;。<br />
《素問&middot;髒氣法時論》說：&ldquo;肺主秋&hellip;&hellip;肺收斂，急食酸以收之，用酸補之，辛瀉之&rdquo;。可見酸味收斂肺氣，辛味發散瀉肺，秋天宜收不宜散，所以要儘量少吃蔥、姜等辛味之品，適當多食酸味果蔬。<br />
總之，秋季時節，可適當食用芝麻、糯米、粳米、蜂蜜、枇杷、鳳梨、乳品等柔潤食物，以益胃生津。</p>
