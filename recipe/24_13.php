	<h3>由來</h3>
<p>每年8月7日或8日視太陽到達黃經135&deg;時為立秋。立秋的&ldquo;立&rdquo;是開始的意思，&ldquo;秋&rdquo;是指莊稼成熟的時期。立秋表示暑去涼來，秋天開始之意。是一個反映季節的節氣。<br />
據《月令七十二候集解》：&ldquo;秋，揪也，物於此而揪斂也&rdquo;。立秋不僅預示著炎熱的夏天即將過去，秋天即將來臨。也表示草木開始結果孕子，收穫季節到了。古人把立秋當作夏秋之交的重要時刻，一直很重視這個節氣。<br />
我國古代將立秋分為三候：&ldquo;一候涼風至；二候白露生；三候寒蟬鳴。&rdquo;是說立秋過後，颳風時人們會感覺到涼爽，此時的風已不同於暑天中的熱風；接著，大地上早晨會有霧氣產生；並且秋天感陰而鳴的寒蟬也開始鳴叫。</p>

