<h3>習俗</h3>
<p>立秋的主要習俗有：立秋節、摸秋、秋忙會、貼秋膘等。立秋節，也稱七月節。時間在西曆每年8月7日或8日開始。民國以來，在廣大農村中，在立秋這天的白天或夜晚，有預卜天氣涼熱之俗。還有以西瓜、四季豆嘗新、奠祖的風俗。又有在立秋前一日，陳冰瓜，蒸茄脯，煎香薷飲等風俗。<br />
摸秋，夏曆八月十五日，是為中秋節。這天夜裡婚後尚未生育的婦女，在小姑或其他女伴的陪同下，到田野瓜架、豆棚下，暗中摸索摘取瓜豆，故名摸秋。</p>

