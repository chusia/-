<h3>介紹</h3>
<p>立秋，是二十四節氣中的第13個節氣，每年8月8日或9日立秋。&ldquo;秋&rdquo;就是指暑去涼來，意味著秋天的開始。<br />
到了立秋，梧桐樹必定開始落葉，因此才有&ldquo;落一葉而知秋&rdquo;的成語。秋季包括立秋、處暑、白露、秋分、寒露、霜降6個節氣，是由熱轉涼，再由涼轉寒的過渡性季節。<br />
每年8月7日或8日視太陽到達黃經135&deg;時為立秋。《月令七十二候集解》：&ldquo;七月節，立字解見春（立春）。秋，揪也，物於此而揪斂也。&rdquo;立秋一般預示著炎熱的夏天即將過去，秋天即將來臨。立秋後雖然一時暑氣難消，還有&ldquo;秋老虎&rdquo;的餘威，立秋又稱交秋，但總的趨勢是天氣逐漸涼爽。</p>
