<h3>習俗</h3>
<p>立冬與立春、立夏、立秋合稱四立，在古代社會中是個重要的節日，這一天皇帝會率領文武百官到京城的北郊設壇祭祀。在現在，人們在立冬之日，也要慶祝一下。<br />
吃餃子：立冬節氣，有秋收冬藏的含義，我國過去是個農耕社會，勞動了一年的人們，利用立冬這一天要休息一下，順便犒賞一家人一年來的辛苦。有句諺語&ldquo;立冬補冬，補嘴空&rdquo;就是最好的比喻。 在我國南方，立冬人們愛吃些雞鴨魚肉，在臺灣立冬這一天，街頭的&lsquo;羊肉爐&rsquo;、&lsquo;薑母鴨&rsquo;等冬令進補餐廳高朋滿座。許多家庭還會燉麻油雞、四物雞來補充能量。<br />
冬 泳：有意思的是，慶祝立冬的方式現在也有了創新，在哈爾濱，立冬之日，黑龍江省冬泳協會的健兒橫渡松花江，以這種方式迎接冬天的到來。<br />
賀冬：賀冬亦稱&ldquo;拜冬&rdquo;，在漢代即有此俗。<br />
冬學 ：冬天夜裡最長，而且又是農閒季節，在這個季節辦&ldquo;冬學&rdquo;是最好的時間。</p>
