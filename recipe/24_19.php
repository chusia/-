	<h3>由來</h3>
<p>我國古代將立冬分為三候：&ldquo;一候水始冰；二候地始凍；三候雉入大水為蜃。&rdquo; 此節氣水已經能結成冰；土地也開始凍結；三候&ldquo;雉入大水為蜃&rdquo;中的雉即指野雞一類的大鳥，蜃為大蛤，立冬後，野雞一類的大鳥便不多見了，而海邊卻可以看到外殼與野雞的線條及顏色相似的大蛤。所以古人認為雉到立冬後便變成大蛤了。<br />
對&ldquo;立冬&rdquo;的理解，我們還不能僅僅停留在冬天開始的意思上。追根溯源，古人對 &ldquo;立&rdquo;的理解與現代人一樣，是建立、開始的意思。但&ldquo;冬&rdquo;字就不那麼簡單了，在古籍《月令七十二候集解》中對&ldquo;冬&rdquo;的解釋是：&ldquo;冬，終也，萬物收藏也&rdquo;，意思是說秋季作物全部收曬完畢，收藏入庫，動物也已藏起來準備冬眠。完整地說，立冬是表示冬季開始，萬物收藏，規避寒冷的意思。</p>
