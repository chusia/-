<h3>介紹</h3>
<p>&ldquo;立冬&rdquo;節氣在每年的11月7日或8日，我國古時民間習慣以立冬為冬季的開始，我國幅員廣大，除全年無冬的華南沿海和長冬無夏的青藏高原地區外，各地的冬季並不都是于立冬日同時開始的。<br />
按氣候學劃分四季標準，以下半年候平均氣溫降到10℃以下為冬季，則&ldquo;立冬為冬日始&rdquo;的說法與黃淮地區的氣候規律基本吻合。我國最北部的漠河及大興安嶺以北地區，9月上旬就早已進入冬季，首都北京於10月下旬也已一派冬天的景象，而長江流域的冬季要到&ldquo;小雪&rdquo;節氣前後才真正開始。</p>
