<h3>養生</h3>
<p>立冬以後，天氣轉寒，如何增強對寒冷的抗禦能力？不外乎使體內產熱增加，散熱減少，其方法與衣食住行無不相關。 調整飲食增加熱量是其中的方法之一。寒冷的環境，適當進食高熱量食品，能促進糖、脂肪、蛋白質的分解代謝。<br />
冬季飲食調養，宜用滋陰潛陽、熱量較高的膳食，也要多吃新鮮蔬菜水果以避免維生素和礦物質缺乏。碳水化合物、脂肪都是產生熱能的營養素，食物中的碳水化合物主要來自糧食和薯類，穀類食物不能減少，成年人日需400-500克。<br />
冬要溫補：少食生冷，但也不宜躁熱，有的放矢地食用一些滋陰潛陽，熱量較高的膳食為宜，同時也要多吃新鮮蔬菜，以避免維生素的缺乏，如：牛羊肉、烏雞、鯽魚，多飲豆漿、牛奶，多吃蘿蔔、青菜、豆腐、木耳等。</p>

