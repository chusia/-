	<h3>養生</h3>
<p>穀雨到來，暮春到了，萬物生長漸旺，天氣也漸熱。中午氣溫較高，但早晚氣溫仍較低，因此早晚時要適當加穿衣服，尤其要注意切勿大汗後吹風，以防感冒；過敏體質的人外出時還要預防花粉過敏，預防過敏性鼻炎、過敏性哮喘等的發作。<br />
此時陽氣漸長，陰氣漸消，要早睡早起，不要過度出汗，以調養髒氣。另外，由於穀雨時節雨水較多，要防濕邪侵入人體，出現肩頸痛、關節疼痛、脘腹脹滿、不欲飲食等病症。<br />
在情志、運動方面也如清明節養生一樣，保持心情舒暢、心胸寬廣，聽音樂、釣魚、春遊、太極拳、散步等都能陶冶性情，切忌遇事憂愁焦慮，甚至動肝火。<br />
此時，肝臟氣伏，心氣逐漸旺盛，脾氣也處於旺盛時期，正是身體補益的大好時機，但不能像冬天一樣進補，應當食用一些益肝補腎的食物，以順應陰陽的變化，為安然度過盛夏打下基礎。</p>
