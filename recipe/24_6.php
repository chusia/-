	<h3>介紹</h3>
<p>穀雨，位於二十四節氣表的第六個節氣。<br />
《月令七十二候集解》： &ldquo;三月中，自雨水後，土膏脈動，今又雨其谷于水也。穀雨時節作去聲，如雨我公田之雨。蓋穀以此時播種，自上而下也。&rdquo;這時天氣溫和，雨水明顯增多，對穀類作物的生長發育關係很大。<br />
雨水適量有利於越冬作物的返青拔節和春播作物的播種出苗，古代所謂&ldquo;雨生百穀&rdquo;，反映了&ldquo;穀雨&rdquo;的現代農業氣候意義。但雨水過量或嚴重乾旱，則往往造成危害，影響後期產量。穀雨在黃河中下游，不僅指明了它的農業意義，也說明了&ldquo;春雨貴如油&rdquo;。<br />
中國古代將穀雨分為三候：&ldquo;第一候萍始生；第二候嗚鳩拂其羽；第三候為戴勝降于桑。&rdquo;是說穀雨後降雨量增多，浮萍開始生長，接著布穀鳥便開始提醒人們播種了，然後是桑樹上開始見到戴勝鳥。</p>
