	<h3>習俗</h3>
<p>1、禁殺五毒<br />
穀雨以後氣溫升高，病蟲害進入高繁衍期，為了減輕病蟲害對作物及人的傷害，農家一邊進田滅蟲，一邊張貼穀雨貼，進行驅凶納吉的祈禱。<br />
2、漁家流行穀雨祭海<br />
穀雨時節正是春海水暖之時，百魚行至淺海地帶，是下海捕魚的好日子。俗話說&ldquo;騎著穀雨上網場&rdquo;。為了能夠出海平安、滿載而歸，穀雨這天漁民要舉行海祭，祈禱海神保佑。<br />
3、走穀雨<br />
古時有&ldquo;走穀雨&rdquo;的風俗，穀雨這天青年婦女走村串親，或者到野外走走，寓意與自然相融合，強身健體。<br />
4、喝穀雨茶<br />
傳說穀雨這天的茶喝了會清火，辟邪，明目等，所以南方有穀雨摘茶習俗。<br />
5、穀雨食香椿<br />
北方則有穀雨食香椿的習俗。<br />
6、穀雨祭祀文祖倉頡<br />
&ldquo;穀雨祭倉頡&rdquo;，是自漢代以來流傳千年的民間傳統。<br />
7、洗澡消災避禍<br />
在西北地方，舊時，人們將穀雨的河水稱為&ldquo;桃花水&rdquo;，傳說以它洗浴，可消災避禍。</p>
