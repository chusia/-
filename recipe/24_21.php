	<h3>由來</h3>
<p>每年12月7日或8日，太陽黃經達255度時為二十四節氣之一的&ldquo;大雪&rdquo;。大雪，顧名思義，雪量大。古人雲：&ldquo;大者，盛也，至此而雪盛也&rdquo;。到了這個時段，雪往往下得大、範圍也廣，故名大雪。 <br />
<br />
這時我國大部分地區的最低溫度都降到了0℃或以下。往往在強冷空氣前沿冷暖空氣交鋒的地區，會降大雪，甚至暴雪。可見，大雪節氣是表示這一時期，降大雪的起始時間和雪量程度，它和小雪、雨水、穀雨等節氣一樣，都是直接反映降水的節氣。 <br />
<br />
我國古代將大雪分為三候：&ldquo;一候鵑鷗不嗚；二候虎始交；三候荔挺出。&rdquo;這是說此時因天氣寒冷，寒號鳥也不再嗚叫了；由於此時是陰氣最盛時期，正所謂盛極而衰，陽氣已有所萌動，所以老虎開始有求偶行為；&ldquo;荔挺&rdquo;為蘭草的一種，也感到陽氣的萌動而抽出新芽。<br />
氣候：大雪時節，黃河流域一帶已有積雪，天氣寒冷。而江南才剛剛進入隆冬季節，氣溫將顯著下降，天氣寒冷，可能出現初雪和冰凍現象。這一節氣是我縣降水日數最少的節氣。<br />
農事：各地要加強冬小麥、油菜等春花作物的田間管理，查苗補缺，中耕鬆土，早施苗肥，培育壯苗。隨著隆冬季節的臨近，著手做好菜苗、果樹、苗木、花卉與家畜的保暖工作。<br />
日期計算：[Y*D+C]-L <br />
<br />
公式解讀：Y=年數後2位，D=0.2422，L=閏年數，21世紀C=7.18，20世紀=7.9。<br />
舉例說明：2088年大雪日期=[88&times;0.2422+7.18]-[88/4]=28-22=6，12月6日大雪。<br />
此時，我國北方患感冒人較多，如能服點大雪順安養生湯，對抵抗寒邪襲之體表、口鼻很有益處。具體方法如下： <br />
<br />
第一、在大雪前中後三天的酉時（17-19時，腎經主時）：食用大雪養生湯。具體方法為：將冬蟲夏草3克、狗肉250克、上肉桂3克，用水煎煮（可放調味品），將狗肉和湯一塊服下。祛寒育腎。 <br />
<br />
第二、從中醫養生學的角度看，大雪已到了&ldquo;進補&rdquo;的大好時節。在大雪至冬至期間，可食用下列食品： 枸杞肉絲、木耳冬瓜三鮮湯。</p>
