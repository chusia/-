<h3>介紹</h3>
<p>&ldquo;大雪&rdquo;節氣，在每年的12月7日或8日，其時視太陽到達黃經255度。《月令七十二候集解》說：&ldquo;至此而雪盛也。&rdquo;大雪的意思是天氣更冷，降雪的可能性比小雪時更大了，並不指降雪量一定很大。相反，大雪後各地降水量均進一步減少，東北、華北地區12月平均降水量一般只有幾毫米，西北地方則不到1毫米；大雪，雪的大小按降雪量分類時，一般降雪量5.0～10毫米。</p>
