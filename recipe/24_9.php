	<h3>養生</h3>
<p>進入小滿後，氣溫不斷升高，人們往往喜愛用冷飲消暑降溫，但冷飲過量會導致腹痛、腹瀉等病症。此時進食生冷飲食易引起胃腸不適而出現腹痛、腹瀉等症，由於小兒消化系統發育尚未健全，老人臟腑機能逐漸衰退，故小孩及老人更易出現此種情況。因此，飲食方面要注意避免過量進食生冷食物。<br />
<br />
另外，小滿後不但天氣炎熱，汗出較多，雨水也較多，飲食調養宜以清爽清淡的素食為主，常吃具有清利濕熱、養陰作用的食物，如赤小豆、薏苡仁、綠豆、冬瓜、黃瓜、黃花菜、水芹、荸薺、黑木耳、胡蘿蔔、番茄、西瓜、山藥、鯽魚、草魚、鴨肉等，忌吃膏粱厚味、甘肥滋膩、生濕助濕的食物，當然也可配合藥膳進行調理，還可以常飲些生脈飲以益氣生津。</p>
