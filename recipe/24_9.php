	<h3>由來</h3>
<p>芒種是表徵麥類等有芒作物的成熟，是一個反映農業物候現象的節氣。時至芒種四川盆地麥收季節已經過去，中稻、紅苕移栽接近尾聲。大部地區中稻進入返青階段，秧苗嫩綠，一派生機。&ldquo;東風染盡三千頃，折鷺飛來無處停&rdquo;的詩句，生動的描繪了這時田野的秀麗景色。<br />
到了芒種時節，盆地內尚未移栽的中稻，應該抓緊栽插；如果再推遲，因氣溫提高，水稻營養生長期縮短，而且生長階段又容易遭受乾旱和病蟲害，產量必然不高。紅苕移栽至遲也要趕在夏至之前；如果栽苕過遲，不但乾旱的影響會加重，而且待到秋來時溫度下降，不利於薯塊膨大，產量亦將明顯降低。農諺&quot;芒種忙忙栽&quot;的道理就在這裡。</p>
