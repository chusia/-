<h3>習俗</h3>
<p>送花神農曆二月二花朝節上迎花神。芒種已近五月間，百花開始凋殘、零落，民間多在芒種日舉行祭祀花神儀式，餞送花神歸位，同時表達對花神的感激之情，盼望來年再次相會。<br />
此俗今已不存，但從著名小說家曹雪芹的《紅樓夢》第二十七回中可窺見一斑：&ldquo;（大觀園中）那些女孩子們，或用花瓣柳枝編成轎馬的，或用綾錦紗羅疊成千旄旌幢的，都用彩線系了。每一棵樹上，每一枝花上，都系了這些物事。滿園裡繡帶飄飄，花枝招展，更兼這些人打扮得桃羞杏讓，燕妒鶯慚，一時也道不盡。&hellip;&hellip;&rdquo;由此可見大戶人家芒種節為花神餞行的熱鬧場面。<br />
安苗系皖南的農事習俗活動，始于明初。每到芒種時節，種完水稻，為祈求秋天有個好收成，各地都要舉行安苗祭祀活動。家家戶戶用新麥面蒸發包，把面捏成五穀六畜、瓜果蔬菜等形狀，然後用蔬菜汁染上顏色，作為祭祀供品，祈求五穀豐登、村民平安。</p>
