	<h3>介紹</h3>
<p>芒種，是農作物成熟的意思。芒種是二十四節氣中的第九個節氣。每年的6月5日左右，太陽到達黃經75&deg;時為芒種。<br />
《月令七十二候集解》：&ldquo;五月節，謂有芒之種穀可稼種矣&rdquo;。意指大麥、小麥等有芒作物種子已經成熟，搶收十分急迫。晚穀、黍、稷等夏播作物也正是播種最忙的季節，故又稱&ldquo;芒種&rdquo;。<br />
春爭日，夏爭時，&ldquo;爭時&rdquo;即指這個時節的收種農忙。人們常說&ldquo;三夏&rdquo;大忙季節，即指忙於夏收、夏種和春播作物的夏管。所以，&ldquo;芒種&rdquo;也稱為&ldquo;忙種&rdquo;&ldquo;忙著種&rdquo;，是農民朋友的播種。&ldquo;芒種&rdquo;到來預示著農民開始了忙碌的田間生活。</p>
