	<h3>介紹</h3>
<p>驚蟄，是24節氣中的第三個節氣。每年3月5日或6日，太陽到達黃經345度時即為驚蟄，這時氣溫回升較快，漸有春雷萌動，驚蟄的意思是天氣回暖，春雷始鳴，驚醒蟄伏於地下冬眠的昆蟲。蟄是藏的意思。 <br />
《月令七十二候集解》中說：&ldquo;二月節，萬物出乎震，震為雷，故曰驚蟄。是蟄蟲驚而出走矣。&rdquo;晉代詩人陶淵明有詩曰：&ldquo;促春遘（g&ograve;u）時雨，始雷發東隅，眾蟄各潛駭，草木縱橫舒。&rdquo;實際上，昆蟲是聽不到雷聲的，大地回春，天氣變暖才是使它們結束冬眠，&ldquo;驚而出走&rdquo;的原因。<br />
我國古代將驚蟄分為三候：&ldquo;一候桃始華；二候倉庚（黃鸝）鳴；三候鷹化為鳩。&rdquo;描述已是進入仲春，桃花紅、李花白，黃鶯嗚叫、燕飛來的時節。按照一般氣侯規律，驚蟄前後各地天氣已開始轉暖，雨水漸多，大部分地區都已進入了春耕。</p>
	
