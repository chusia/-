	<h3>由來</h3>
<p>和其他節氣相同，驚蟄日期也和農曆沒有必然聯繫。每年3月4日～7日，太陽到達黃經345度時為&ldquo;驚蟄&rdquo;。驚蟄的意思是天氣回暖，春雷始鳴，驚醒蟄伏於地下冬眠的昆蟲。<br />
驚蟄雷鳴最引人注意。如&ldquo;未過驚蟄先打雷，四十九天雲不開&rdquo;。驚蟄節氣正處乍寒乍暖之際，根據冷暖預測後期天氣的諺語有：&ldquo;冷驚蟄，暖春分&rdquo;等。驚蟄節的風也有用來作預測後期天氣的依據。如&ldquo;驚蟄刮北風，從頭另過冬&rdquo;、&quot;驚蟄吹南風，秧苗遲下種&quot;。<br />
現代氣象科學表明，&ldquo;驚蟄&rdquo;前後，之所以偶有雷聲，是大地濕度漸高而促使近地面熱氣上升或北上的濕熱空氣勢力較強與活動頻繁所致。</p>
