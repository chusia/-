	<h3>習俗</h3>
<p>1、祭白虎化解是非<br />
中國的民間傳說白虎是口舌、是非之神，每年都會在這天出來覓食，開口噬人，犯之則在這年之內，常遭邪惡小人對你興波作浪，阻撓你的前程發展，引致百般不順。大家為了自保，便在驚蟄那天祭白虎。 <br />
所謂祭白虎，是指拜祭用紙繪製的白老虎，紙老虎一般為黃色黑斑紋，口角畫有一對獠牙。拜祭時，需以肥豬血喂之，使其吃飽後不再出口傷人，繼而以生豬肉抹在紙老虎的嘴上，使之充滿油水，不能張口說人是非。<br />
2、&ldquo;打小人&rdquo;驅趕黴運<br />
驚蟄象徵二月份的開始，會平地一聲雷，喚醒所有冬眠中的蛇蟲鼠蟻，家中的爬蟲走蟻又會應聲而起，四處覓食。所以古時驚蟄當日，人們會手持清香、艾草，熏家中四角，以香味驅趕蛇、蟲、蚊、鼠和黴味，久而久之，漸漸演變成不順心者拍打對頭人和驅趕黴運的習慣，亦即&ldquo;打小人&rdquo;的前身。</p>
