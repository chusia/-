	<h3>養生</h3>
<p>驚蟄時節人體的肝陽之氣漸升，陰血相對不足，養生應順乎陽氣的升發、萬物始生的特點，使自身的精神、情志、氣血也如春日一樣舒展暢達，生機盎然。<br />
從飲食方面來看，驚蟄時節飲食起居應順肝之性，助益脾氣，令五臟和平。宜多吃富含植物蛋白質、維生素的清淡食物，少食動物脂肪類食物。可多食鴨血、菠菜、蘆薈、水蘿蔔、苦瓜、木耳菜、芹菜、油菜、山藥、蓮子、銀耳等食物。<br />
由於春季與肝相應，如養生不當則可傷肝。現代流行病學調查亦證實，驚蟄屬肝病的高發季節。此外，諸如流感、流腦、水痘、帶狀皰疹、流行性出血熱等在這一節氣都易流行爆發，因此要嚴防此類疾病。</p>
