<h3>養生</h3>
<p>霜降節氣是慢性胃炎和胃十二指腸潰瘍病復發的高峰期。老年人也極容易患上&ldquo;老寒腿&rdquo;（膝關節骨性關節炎）的毛病，慢性支氣管炎也容易復發或加重。這時應該多吃些梨，蘋果，白果，洋蔥，芥菜(雪裡蕻)。<br />
霜降之時，在五行中屬土，根據中醫養生學的觀點，在四季五補（春要升補、夏要清補、長夏要淡補、秋要平補、冬要溫補）的相互關係上，此時與長夏同屬土，所以應以淡補為原則，並且要補血氣以養胃。<br />
飲食進補當依據食物的性味、歸經加以區別。飲食養生學側重於根據食物的&ldquo;性味歸經&rdquo;來調節人體陰陽，滋養五臟六腑和預防疾病。因此，在傳統養生學中，食物也根據其&ldquo;性味歸經&rdquo;而分為了不同的類型。</p>

