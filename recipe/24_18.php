	<h3>由來</h3>
<p>每年陽曆10月23日前後，太陽到達黃經210度時為二十四節氣中的霜降。霜降是秋季的最後一個節氣，是秋季到冬季的過渡節氣。秋晚地面上散熱很多，溫度驟然下降到0度以下，空氣中的水蒸氣在地面或植物上直接凝結形成細微的冰針，有的成為六角形的霜花，色白且結構疏鬆。<br />
《逸周書&bull;周月》：&ldquo;秋三月中氣：處暑、秋分、霜降。&rdquo;《國語&bull;周語中》：&ldquo;火見而清風戒寒。&rdquo; 三國 吳 韋昭 注：&ldquo;謂霜降之後，清風先至，所以戒人為寒備也。&rdquo;魏巍 《東方》第一部第十五章：&ldquo;論節氣，還不到霜降，這裡已經下了好幾場霜。&rdquo; 古籍《二十四節氣解》中說：&ldquo;氣肅而霜降，陰始凝也。&rdquo;可見&ldquo;霜降&rdquo;表示天氣逐漸變冷，開始降霜。氣象學上，一般把秋季出現的第一次霜叫做&ldquo;早霜&rdquo;或&ldquo;初霜&rdquo;，而把春季出現的最後一次霜稱為&ldquo;晚霜&rdquo;或&ldquo;終霜&rdquo;。從終霜到初霜的間隔時期，就是無霜期。</p>
