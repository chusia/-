<h3>介紹</h3>
<p>霜降二十四節氣之一，天氣漸冷，開始有霜。霜降一般是在每年西曆的10月23日。這時中國黃河流域一帶出現初霜﹐大部分地區多忙於播種三麥等作物。<br />
我國古代將霜降分為三候：&ldquo;一候豺乃祭獸；二候草木黃落；三候蜇蟲鹹俯。&rdquo; 此節氣中豺狼將捕獲的獵物先陳列後再食用；大地上的樹葉枯黃掉落；蜇蟲也全在洞中不動不食，垂下頭來進入冬眠狀態中。<br />
每年陽曆10月23日前後，太陽到達黃經210度時為霜降。霜降表示天氣更冷了，露水凝結成霜。《月令七十二候集解》：&ldquo;九月中，氣肅而凝，露結為霜矣&rdquo;。</p>
