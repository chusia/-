<h3>習俗</h3>
<p>霜降是秋季的最後一個節氣。在此期間，我國很多地區都有吃柿子的習俗。俗話說；&ldquo;霜降吃柿子，不會流鼻涕。&rdquo; <br />
山西：時值寒露抓秋耕，秋收秋種莫放鬆。采棉刨薯回茬麥結合秋澆快進行。澆地造林集飼料，山藥異地換種子。<br />
河北：寒露霜降，收割大豆。抓緊打場，及時入庫。晚茬小麥，突擊播種。收割山草，好餵牲口。菠菜油菜，種上幾畝。來年春季，能早收穫。<br />
山東：霜降一到，天氣漸冷。抓緊收割，地瓜花生。切曬瓜幹，要趁晴天。地瓜入窖，不能放鬆。麥田苗情，檢查要精。缺苗斷壟，及時補種。<br />
<br />
上海：十月寒露接霜降，秋收秋種冬活忙，晚稻脫粒棉翻曬，精收細打妥收藏。安徽，寒露收割罷，霜降把地翻。湖南：十月寒露霜降到，收割晚稻又挖薯。</p>
