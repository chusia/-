	<h3>由來</h3>
<p>我國古代將小暑分為三候：&ldquo;一候溫風至；二候蟋蟀居宇；三候鷹始鷙。&rdquo;小暑時節大地上便不再有一絲涼風，而是所有的風中都帶著熱浪；《詩經&bull;七月》中描述蟋蟀的字句有&ldquo;七月在野，八月在宇，九月在戶，十月蟋蟀入我床下。&rdquo;文中所說的八月即是夏曆的六月，即小暑節氣的時候，由於炎熱，蟋蟀離開了田野，到庭院的牆角下以避暑熱；在這一節氣中，老鷹因地面氣溫太高而在清涼的高空中活動。</p>

