	<h3>養生</h3>
<p>小暑是二十四節氣的第11個節氣，也是人體陽氣最旺盛的時候，&ldquo;春夏養陽&rdquo;。所以人們在工作勞動之時，要注意勞逸結合，保護人體的陽氣。<br />
&ldquo;熱在三伏&rdquo;，此時正是進入伏天的開始。&ldquo;伏&rdquo;即伏藏的意思，所以人們應當少外出以避暑氣。民間度過伏天的辦法，就是吃清涼消暑的食品。俗話說&ldquo;頭伏餃子二伏面，三伏烙餅攤雞蛋&rdquo;。這種吃法便是為了使身體多出汗，排出體內的各種毒素。天氣熱的時候要喝粥，用荷葉、土茯苓、扁豆、薏米、豬苓、澤瀉、木棉花等材料煲成的消暑湯或粥，或甜或咸，非常適合此節氣食用，多吃水果也有益的防暑，但是不要食用過量，以免增加腸胃負擔，嚴重的會造成腹瀉。民間還有：&ldquo;冬不坐石，夏不坐木。&rdquo;的說法。<br />
小暑過後，氣溫高、濕度大。久置露天裡的木料，如椅凳等，經過露打雨淋，含水分較多，表面看上去是幹的，可是經太陽一曬，溫度升高，便會向外散發潮氣，在上面坐久了，能誘發痔瘡、風濕和關節炎等疾病。所以，尤其是中老年人，一定要注意不能長時間坐在露天放置的木料上。</p>
