<h3>介紹</h3>
<p>每年7月7日或8日視太陽到達黃經105&deg;時為小暑（slight heat）。《月令七十二候集解》：&ldquo;六月節&hellip;&hellip;暑，熱也，就熱之中分為大小，月初為小，月中為大，今則熱氣猶小也。&rdquo;暑，表示炎熱的意思，小暑為小 小暑養生熱，還不十分熱。意指天氣開始炎熱，但還沒到最熱，全國大部分地區基本符合。<br />
這時江淮流域梅雨即將結束，盛夏開始，氣溫升高，並進入伏旱期;而華北、東北地區進入多雨季節，熱帶氣旋活動頻繁，登陸我國的熱帶氣旋開始增多。小暑後南方應注意抗旱，北方須注意防澇。全國的農作物都進入了茁壯成長階段，需加強田間管理。小暑的標誌;出梅。入伏。</p>
