<h3>養生</h3>
<p>秋分節氣已經真正進入到秋季，作為晝夜時間相等的節氣，人們在養生中也應本著陰陽平衡的規律，使機體保持&ldquo;陰平陽秘&rdquo;的原則，按照《素問?至真要大論》所說：&ldquo;謹察陰陽之所在，以平為期&rdquo;，陰陽所在不可出現偏頗。要想保持機體的陰陽平衡，首先要防止外界邪氣的侵襲。<br />
同時，秋燥溫與涼的變化，還與每個人的體質和機體反應有關。要防止涼燥，就得堅持鍛煉身體，增強體質，提高抗病能力。秋季鍛煉，重在益肺潤燥，如練吐納功、叩齒咽津潤燥功。飲食調養方面，應多喝水，吃清潤、溫潤的食物，如芝麻、核桃、糯米、蜂蜜、乳品、梨等，可以起到滋陰潤肺、養陰生津的作用。</p>
