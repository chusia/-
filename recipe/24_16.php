<h3>習俗</h3>
<p>秋分曾是傳統的&ldquo;祭月節&rdquo;。如古有&ldquo;春祭日，秋祭月&rdquo;之說。現在的中秋節則是由傳統的&ldquo;祭月節&rdquo;而來。據考證，最初&ldquo;祭月節&rdquo;是定在&ldquo;秋分&rdquo;這一天，不過由於這一天在農曆八月裡的日子每年不同，不一定都有圓月。而祭月無月則是大煞風景的。所以，後來就將&ldquo;祭月節&rdquo;由&ldquo;秋分&rdquo;調至中秋。<br />
秋分這一天，我國很多地方的農村還有煮湯圓吃的習俗，除了自己食用外，還要煮二三十個不用包心的湯圓，插上細竹簽放在田邊地頭，這就是&ldquo;粘雀子嘴&rdquo;，寓意是讓雀子不要來破壞莊稼。<br />
在秋分時節，我國很多地區都要舉行&ldquo;豎蛋&rdquo;的民俗活動。<br />
很多地方在秋分時節要吃一種叫做&ldquo;野莧菜&rdquo;的野菜，有的地方也稱之為&ldquo;秋碧蒿&rdquo;。秋分一到，全家人都去採摘秋菜。采回的秋菜一般人家與魚片&ldquo;滾湯&rdquo;，燉出來的湯叫做&ldquo;秋湯&rdquo;。有順口溜這樣說：&ldquo;秋湯灌髒，洗滌肝腸。闔家老少，平安健康。&rdquo;</p>

