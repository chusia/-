	<h3>時間</h3>
<p>每年09月22~24日</p>
<h3>三候</h3>
<p>雷始收聲：古人認為雷是因為陽氣盛而發聲，秋分後陰氣開始旺盛，所以不再打雷了。<br />
蟄蟲坯戶：一些春分出土活動的小蟲，在秋分過後也會陸陸續續回到土裡，準備過冬。<br />
水始涸：水氣也不像夏天時那麼充沛，將開始逐漸乾涸，因此我們會覺得空氣越來越乾燥了。</p>
