	<h3>由來</h3>
<p>秋分（autumnal equinox），農曆二十四節氣中的第十六個節氣，時間一般為每年的9月22或23日。南方的氣候由這一節氣起才始入秋。<br />
我國古籍《春秋繁露?陰陽出入上下篇》中說：&ldquo;秋分者，陰陽相半也，故晝夜均而寒暑平。&rdquo;秋分之&ldquo;分&rdquo;為&ldquo;半&rdquo;之意。 秋分&ldquo;秋分&rdquo;的意思有二：<br />
一是太陽在這一天到達黃經180度，直射地球赤道，因此這一天24小時晝夜均分，各12小時；全球無極晝極夜現象。秋分之後，北極附近極夜範圍漸大，南極附近極晝範圍漸大。<br />
二是按我國古代以立春、立夏、立秋、立冬為四季開始的季節劃分法，秋分日居秋季90天之中，平分了秋季。</p>

