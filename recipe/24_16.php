<h3>介紹</h3>
<p>按農曆來講，&ldquo;立秋&rdquo;是秋季的開始，到&ldquo;霜降&rdquo;為秋季終止，而&ldquo;秋分&rdquo;正好是從立秋到霜降90天的一半。<br />
秋分後太陽直射的位置移至南半球，北半球得到的太陽輻射越來越少，而地面散失的熱量卻較多，氣溫降低的速度明顯加快。農諺說：&ldquo;一場秋雨一場寒&rdquo;，&ldquo;白露秋分夜，一夜冷一夜&rdquo;。&ldquo;八月雁門開，雁兒腳下帶霜來&rdquo;，東北地區降溫早的年分，秋分見霜已不足為奇。<br />
從秋分這一天起，氣候主要呈現三大特點：陽光直射的位置繼續由赤道向南半球推移，北半球晝短夜長的現象將越來越明顯，白天逐漸變短，黑夜變長（直至冬至日達到黑夜最長，白天最短）；晝夜溫差逐漸加大，幅度將高於10℃以上；氣溫逐日下降，一天比一天冷，逐漸步入深秋季節。南半球的情況則正好相反。</p>
