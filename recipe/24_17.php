<h3>介紹</h3>
<p>寒露在二十四節氣中排列十七，於每年的十月八日至九日交節。《月令七十二候集解》說：&ldquo;九月節，露氣寒冷，將凝結也。&rdquo;寒露的意思是氣溫比白露時更低，地面的露水更冷，快要凝結成霜了。寒露時節，南嶺及以北的廣大地區均已進入秋季，東北和西北地方已進入或即將進入冬季。<br />
白露後，天氣轉涼，開始出現露水，到了寒露，則露水增多，且氣溫更低。此時我國有些地區會出現霜凍，北方已呈深秋景象，白雲紅葉，偶見早霜，南方也秋意漸濃，蟬噤荷殘。<br />
古代把露作為天氣轉涼變冷的表徵。仲秋白露節氣&quot;露凝而白&quot;，至季秋寒露時已是&quot;露氣寒冷，將凝結&quot;為霜了。</p>
