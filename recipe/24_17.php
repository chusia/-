<h3>養生</h3>
<p>&ldquo;寒露&rdquo;時節起，雨水漸少，天氣乾燥，晝熱夜涼。從中醫角度上說，這節氣在南方氣候最大的特點是&ldquo;燥&rdquo;邪當令，而燥邪最容易傷肺傷胃。此時期人們的汗液蒸發較快，因而常出現皮膚乾燥，皺紋增多，口乾咽燥，乾咳少痰，甚至會毛髮脫落和大便秘結等。所以養生的重點是養陰防燥、潤肺益胃。<br />
要避免因劇烈運動、過度勞累等耗散精氣津液。在飲食上還應少吃辛辣刺激、香燥、熏烤等類食品，宜多吃些芝麻、核桃、銀耳、蘿蔔、番茄、蓮藕、牛奶、百合、沙參等有滋陰潤燥、益胃生津作用的食品。同時室內要保持一定的濕度，注意補充水分，多吃雪梨、香蕉、哈密瓜、蘋果、水柿、提子等水果。此外還應重視塗擦護膚霜等以保護皮膚，防止乾裂。</p>

