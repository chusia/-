	<h3>由來</h3>
<p>每年10月8日或9日視太陽到達黃經195&deg;時為寒露。《月令七十二候集解》說：&ldquo;九月節，露氣寒冷，將凝結也。&rdquo;寒露的意思是氣溫比白露時更低，地面的露水更冷，快要凝結成霜了。寒露時節，南嶺及以北的廣大地區均已進入秋季，東北和西北地方已進入或即將進入冬季。<br />
我國古代將寒露分為三候：&ldquo;一候鴻雁來賓；二候雀入大水為蛤；三候菊有黃華。&rdquo; 此節氣中鴻雁排成一字或人字形的佇列大舉南遷；深秋天寒，雀鳥都不見了，古人看到海邊突然出現很多蛤蜊，並且貝殼的條紋及顏色與雀鳥很相似，所以便以為是雀鳥變成的；第三候的&ldquo;菊始黃華&rdquo;是說在此時菊花已普遍開放。</p>

