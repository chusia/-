<h3>習俗</h3>
<p>登高習俗<br />
如果說白露時節天氣轉涼，開始出現露水，那麼到了寒露，則露水增多，且氣溫更低。此時我國有些地區會出現霜凍，北方已呈深秋景象，白雲紅葉，偶見早霜，南方也秋意漸濃，蟬噤荷殘。北京人登高習俗更盛，景山公園、八大處、香山等都是登高的好地方，重九登高節，更會吸引眾多的遊人。<br />
農事習俗<br />
寒露時天氣對秋收十分有利，農諺有：黃煙花生也該收，起捕成魚采藕芡。大豆收割寒露天，石榴山楂摘下來。寒露蜜桃屬北方晚熟桃品種，成熟期在寒露前後，故名&quot;寒露蜜桃&quot;。<br />
飲食習俗<br />
寒露時節，應多食用芝麻、糯米、粳米、蜂蜜、乳製品等柔潤食物，同時增加雞、鴨、牛肉、豬肝、魚、蝦、大棗、山藥等以增加體質；少食辛辣之品，如辣椒、生薑、蔥、蒜類，因過食辛辣宜傷人體陰精。有條件可以煮一點百棗蓮子銀杏粥經常喝，經常吃些山藥和馬蹄也是不錯的養生辦法。</p>

