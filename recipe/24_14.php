<h3>習俗</h3>
<p>1、出遊迎秋<br />
祭祖、迎秋處暑節氣前後的民俗多與祭祖及迎秋有關。處暑前後民間會有慶贊中元的民俗活動，俗稱&ldquo;作七月半&rdquo;或&ldquo;中元節&rdquo;。舊時民間從七月初一起,就有開鬼門的儀式，直到月底關鬼門止，都會舉行普渡佈施活動。此外，處暑之後，秋意漸濃，正是人們暢遊郊野迎秋賞景的好時節。 <br />
2、放河燈<br />
河燈也叫&ldquo;荷花燈&rdquo;，一般是在底座上放燈盞或蠟燭，中元夜放在江河湖海之中，任其漂泛。放河燈是為了普渡水中的落水鬼和其他孤魂野鬼。<br />
3、開漁節<br />
對於沿海漁民來說，處暑以後漁業收穫的時節，因為，這時海域水溫依然偏高，魚群還是會停留在海域周圍，魚蝦貝類發育成熟。因此，從這一時間開始，人們往往可以享受到種類繁多的海鮮。</p>

