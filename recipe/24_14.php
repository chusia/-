<h3>介紹</h3>
<p>處暑，二十四節氣之一。處暑節氣在每年八月二十三日左右。此時太陽到達黃經150&deg;。據《月令七十二候集解》說：&ldquo;處，去也，暑氣至此而止矣。&rdquo;意思是炎熱的夏天即將過去了。雖然，處暑前後我國北京、太原、西安、成都和貴陽一線以東及以南的廣大地區和新疆塔里木盆地地區日平均氣溫仍在攝氏二十二度以上，處於夏季，但是這時冷空氣南下次數增多，氣溫下降逐漸明顯。<br />
我國古代將處暑分為三候：&ldquo;一候鷹乃祭鳥；二候天地始肅；三候禾乃登。&rdquo;此節氣中老鷹開始大量捕處暑養生獵鳥類；天地間萬物開始凋零；&ldquo;禾乃登&rdquo;的&ldquo;禾&rdquo;指的是黍、稷、稻、粱類農作物的總稱，&ldquo;登&rdquo;即成熟的意思。<br />
處暑是反映氣溫變化的一個節氣。&quot;處&quot;含有躲藏、終止意思，&quot;處暑&quot;表示炎熱暑天結束了。也就是說是炎熱的夏天即將過去，到此為止了。</p>
