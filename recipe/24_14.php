	<h3>由來</h3>
<p>每年的8月23日前後（8月22日～24日），視太陽到達黃經150&deg;時是二十四節氣的處暑。處暑是反映氣溫變化的一個節氣。&ldquo;處&rdquo;含有躲藏、終止意思，&ldquo;處暑&rdquo;表示炎熱暑天結束了。<br />
《月令七十二候集解》說：&ldquo;處，去也，暑氣至此而止矣。&rdquo;&ldquo;處&rdquo;是終止的意思，表示炎熱即將過去，暑氣將於這一天結束，我國大部分地區氣溫逐漸下降。處暑既不同於小暑、大暑、也不同于小寒、大寒節氣，它是代表氣溫由炎熱向寒冷過渡的節氣。</p>

