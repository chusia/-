	<h3>養生</h3>
<p>秋天陰氣增、陽氣減，對應人體的陽氣也隨著內收，為了貯存體內陽氣。然而，隨著天氣轉涼，很多人會有懶洋洋的疲勞感，早上不愛起，白天不愛動，這就是&ldquo;春困秋乏&rdquo;中所指的&ldquo;秋乏&rdquo;。要保證充足睡眠，改掉夏季晚睡習慣，爭取晚上10點前入睡，以比夏天增加1小時睡眠為好，並保證早睡早起。另外，適當午睡也利於化解秋乏。<br />
夏天結束了，就意味著秋季的開始。這個時期，氣侯逐漸乾燥，身體裡肺經當值。秋天要多吃些滋陰潤燥的食物，避免燥邪傷害。保持飲食清淡，不吃或少吃辛辣燒烤食物，如辣椒、生薑、花椒、蔥、桂皮、酒等；少吃油膩的肉食。多吃含維生素的食物，如番茄、辣椒、茄子、馬鈴薯、梨等；多吃鹼性食物，如蘋果、海帶以及新鮮蔬菜等。適量增加優 質蛋白質的攝入，如雞蛋、瘦肉、魚、乳製品及豆製品等。此外，還應多喝水，以保持肺臟與呼吸道的正常濕潤度。</p>
