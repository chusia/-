<h3>習俗</h3>
<p>醃臘肉 <br />
<br />
小雪後氣溫急劇下降，天氣變得乾燥，是加工臘肉的好時候。小雪節氣後，一些農家開始動手做香腸、臘肉，等到春節時正好享受美食。 <br />
<br />
吃糍粑 <br />
<br />
在南方某些地方，還有農曆十月吃糍粑的習俗。古時，糍粑是南方地區傳統的節日祭品，最早是農民用來祭牛神的供品。有俗語&ldquo;十月朝，糍粑祿祿燒&rdquo;，就是指的祭祀事件。<br />
<br />
民間有：&ldquo;冬臘風醃，蓄以禦冬&rdquo;的習俗。小雪後氣溫急劇下降，天氣變得乾燥，是加工臘肉的好時候。小雪節氣後，一些人家開始動手做香腸、臘肉。在南方某些地方，還有農曆十月吃糍粑的習俗。</p>	</div> 
