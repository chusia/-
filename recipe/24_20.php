<h3>養生</h3>
<p>孫思邈雲：&ldquo;安身之本，必資於食&hellip;&hellip;不知食宜結，不足以生存也。&rdquo;合理的飲食，可以使人身體強健、益壽延年，而飲食不當則是導致疾病和早衰的重要原因之一。為避免血液黏稠，應多食保護心腦血管的食品，如丹參、山楂、黑木耳、番茄、芹菜、紅心蘿蔔等。<br />
適宜吃降血脂食品，如苦瓜、玉米、蕎麥、胡蘿蔔等。這個季節宜吃溫補性食物和益腎食品。溫補性食物有羊肉、牛肉、雞肉、狗肉、鹿茸等；益腎食品有以腰果、芡實、山藥熬的粥，栗子燉肉、白果燉雞、大骨頭湯、核桃等。另外，要多吃燉食和黑色食品，如黑木耳、黑芝麻、黑豆等。</p>

