<h3>介紹</h3>
<p>每年11月22日23日，視太陽到達黃經240&deg;時為小雪，《月令七十二候集解》有曰：&ldquo;10月中，雨下而為寒氣所薄，故凝而為雪。小者未盛之辭。&rdquo;這個時期天氣逐漸變冷，黃河中下游平均初雪期基本與小雪節令一致。雖然開始下雪，一般雪量較小，並且夜凍晝化。如果冷空氣勢力較強，暖濕氣流又比較活躍的話，也有可能下大雪。如1993年11月15～20日，北方一部分冬麥區就下了大到暴雪。<br />
&ldquo;小雪&rdquo;時值陽曆11月下半月，農曆十月下半月。&ldquo;小雪&rdquo;是反映天氣現象的節令。古籍《群芳譜》中說：&ldquo;小雪氣寒而將雪矣，地寒未甚而雪未大也。&rdquo;這就是說，到&ldquo;小雪&rdquo;節由於天氣寒冷，降水形式由雨變為雪，但此時由於&ldquo;地寒未甚&rdquo;故雪量還不大，所以稱為小雪。</p>
