	<h3>由來</h3>
<p>小雪節氣到&ldquo;小雪&rdquo;是反映天氣現象的節令。雪小，地面上又無積雪，這正是&ldquo;小雪&rdquo;這個節氣的原本之意。古籍《群芳譜》中說：&ldquo;小雪氣寒而將雪矣，地寒未甚而雪未大也。&rdquo;這就是說，到&ldquo;小雪&rdquo;節由於天氣寒冷，降水形式由雨變為雪，但此時由於&ldquo;地寒未甚&rdquo;故雪下得次數少，雪量還不大，所以稱為小雪。因此，小雪表示降雪的起始時間和程度，小雪和雨水、穀雨等節氣一樣，都是直接反映降水的節氣。</p>
