<h3>習俗</h3>
<p>江南一帶則有&ldquo;小滿動三車&rdquo;的說法。它是對江南小滿農事的形象總結。所謂&ldquo;三車&rdquo;即水車、紡車、油車。在農諺中，百姓以&ldquo;滿&rdquo;指代雨水的豐裕程度，小滿正是江南早稻追肥、中稻插秧的時節，如若田裡不蓄滿水，就會造成田坎乾裂，無法插秧，影響農作物的收成。<br />
因此天旱的年份，人們會早考慮，巧安排，以人力或畜力帶動水車灌溉水田。過去行走在偏僻的江南古鎮水田邊，時常會見到水牛蒙住雙眼轉動水車的木車盤帶動龍骨水車提水，或人力雙腳交替踏車提水的情景。</p>
