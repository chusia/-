	<h3>由來</h3>
<p>《月令七十二候集解》：&ldquo;四月中，小滿者，物致於此小得盈滿。&rdquo;這時全國北方地區麥類等夏熟作物籽粒已開始飽滿，但還沒有成熟，約相當乳熟後期，所以叫小滿。<br />
南方地區的農諺賦予小滿以新的寓意：&ldquo;小滿不滿，幹斷思坎&rdquo;；&ldquo;小滿不滿，芒種不管&rdquo;。把&ldquo;滿&rdquo;用來形容雨水的盈缺，指出小滿時田裡如果蓄不滿水，就可能造成田坎乾裂，甚至芒種時也無法栽插水稻。</p>
