	<h3>由來</h3>
<p>春分是從每年的3月20日（或21日）開始至4月4日（或5日）結束。春分，每年西曆3月20或21日，太陽到達黃經0度(春分點)時開始。這天晝夜長短平均，正當春季九十日之半，故稱&ldquo;春分&rdquo;。<br />
春分這一天陽光直射赤道，晝夜幾乎相等，其後陽光直射位置逐漸北移，開始晝長夜短。春分是個比較重要的節氣，它不僅有天文學上的意義：南北半球晝夜平分；在氣候上，也有比較明顯的特徵，春分時節，我國除青藏高原、東北、西北和華北北部地方外都進入明媚的春天，在遼闊的大地上，楊柳青青、鶯飛草長、小麥拔節、油菜花香。<br />
《月令七十二候集解》：&ldquo;(夏曆)二月中，分者半也，此當九十日之半，故謂之分。秋同義。&rdquo;漢董仲舒《春秋繁露?陰陽出入上下》：&ldquo;至於中春之月，陽在正東，陰在正西，謂之春分。春分者，陰陽相半也，故晝夜均而寒暑平。&rdquo;</p>
