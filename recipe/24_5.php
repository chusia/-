	<h3>養生</h3>
<p>由於春分節氣平分了晝夜、寒暑，人們在保健養生時應注意保持人體的陰陽平衡狀態。《素問?骨空論》：&ldquo;調其陰陽，不足則補，有餘則瀉&rdquo;。傳統飲食養生與中醫治療均可概括為補虛、瀉實兩方面。如益氣、養血、滋陰、助陽、填精、生津為補虛；解表、清熱、利水、瀉下、祛寒、去風、燥濕等方面則可視為瀉實。<br />
中醫養生實踐證明，無論補或瀉，都應堅持調整陰陽，以平為期的原則，科學地進行飲食保健，才能有效地防治很多非感染性疾病。<br />
春天到來從立春節氣到清明節氣前後是草木生長萌芽期，人體血液也正處於旺盛時期，激素水準也處於相對高峰期，此時易發常見的非感染性疾病有高血壓、月經失調、痔瘡及過敏性疾病等。<br />
在此節氣的飲食調養，應當根據自己的實際情況選擇能夠保持機體功能協調平衡的膳食，禁忌偏熱、偏寒、偏升、偏降的飲食誤區，如在烹調魚、蝦、蟹等寒性食物時，其原則必佐以蔥、薑、酒、醋類溫性調料，以防止本菜肴性寒偏涼，食後有損脾胃而引起脘腹不舒之弊。</p>
