	<h3>習俗</h3>
<p>1、豎蛋<br />
春分成了豎蛋遊戲的最佳時光，故有&ldquo;春分到，蛋兒俏&rdquo;的說法。<br />
2、吃春菜<br />
&quot;春分吃春菜&quot;。&quot;春菜&quot;是一種野莧菜，鄉人稱之為&quot;春碧蒿&quot;。逢春分那天采回的春菜一般家裡與魚片&quot;滾湯&quot;，名曰&quot;春湯&quot;。<br />
3、送春牛<br />
春分隨之即到，其時便出現挨家送春牛圖的。其圖是把二開紅紙或黃紙印上全年農曆節氣，還要印上農夫耕田圖樣，名曰&quot;春牛圖&quot;。<br />
4、粘雀子嘴<br />
春分這一天農民都按習俗放假，每家都要吃湯元，而且還要把不用包心的湯元十多個或二三十個煮好，用細竹叉扡著置於室外田邊地坎，名曰粘雀子嘴，免得雀子來破壞莊稼。<br />
5、春祭<br />
二月春分，開始掃墓祭祖，也叫春祭。掃墓前先要在祠堂舉行隆重的祭祖儀式，殺豬、宰羊，請鼓手吹奏，由禮生念祭文，帶引行三獻禮。<br />
6、祭日<br />
在周代，春分有祭日儀式。此俗歷代相傳。清潘榮陛《帝京歲時紀勝》：&ldquo;春分祭日，秋分祭月，乃國之大典，士民不得擅祀。&rdquo;</p>
