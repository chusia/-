	<h3>介紹</h3>
<p>春分，古時又稱為&ldquo;日中&rdquo;、&ldquo;日夜分&rdquo;、&ldquo;仲春之月&rdquo;，在每年的3月21日前後(20日～22日)交節，農曆日期不固定，這時太陽到達黃經0&deg;。<br />
據《月令七十二候集解》：&ldquo;二月中，分者半也，此當九十日之半，故謂之分。&rdquo;另《春秋繁露?陰陽出入上下篇》說：&ldquo;春分者，陰陽相半也，故晝夜均而寒暑平。&rdquo;有《明史?曆一》說：&ldquo;分者，黃赤相交之點，太陽行至此，乃晝夜平分。&rdquo;所以，春分的意義，一是指一天時間白天黑夜平分，各為12小時；二是古時以立春至立夏為春季，春分正當春季三個月之中，平分了春季。<br />
中國古代將春分分為三候：&ldquo;一候元鳥至；二候雷乃發聲；三候始電。&rdquo;便是說春分日後，燕子便從南方飛來了，下雨時天空便要打雷並發出閃電。</p>
	
