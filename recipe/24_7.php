	<h3>介紹</h3>
<p>每年5月5日或5月6日是農曆的立夏。&ldquo;鬥指東南，維為立夏，萬物至此皆長大，故名立夏也。&rdquo;此時，太陽黃經為45度，在天文學上，立夏表示即將告別春天，是夏日天的開始。<br />
人們習慣上都把立夏當作是溫度明顯升高，炎暑將臨，雷雨增多，農作物進入旺季生長的一個重要節氣。</p>
<h3>由來</h3>
<p>立夏時節，萬物繁茂。明人《蓮生八戕》一書中寫有：&ldquo;孟夏之日，天地始交，萬物並秀。&rdquo;這時夏收作物進入生長後期，冬小麥揚花灌漿，油菜接近成熟，夏收作物年景基本定局，故農諺有&ldquo;立夏看夏&rdquo;之說。<br />
水稻栽插以及其他春播作物的管理也進入了大忙季節。所以，我國古來很重視立夏節氣。據記載，周朝時，立夏這天，帝王要親率文武百官到郊外&ldquo;迎夏&rdquo;，並指令司徒等官去各地勉勵農民抓緊耕作。</p>
