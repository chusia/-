	<h3>養生</h3>
<p>立夏之後，天氣逐漸轉熱，飲食宜清淡，應以易消化、富含維生素的食物為主，大魚大肉和油膩辛辣的食物要少吃。立夏以後飲食原則是&ldquo;春夏養陽&rdquo;，養陽重在養心，養心可多喝牛奶、多吃豆製品、雞肉、瘦肉等，既能補充營養，又起到強心的作用。平時多吃蔬菜、水果及粗糧，可增加纖維素、維生素B、C的供給，能起到預防動脈硬化的作用。總之立夏之季要養心，為安度酷暑做準備，使身體各臟腑功能正常，以達到&ldquo;正氣充足，邪不可幹&rdquo;的境界。<br />
傳統中醫認為，&ldquo;暑易傷氣&rdquo;，&ldquo;暑易入心&rdquo;。因此，值此時節，人們要重視精神的調養，加強對心臟的保養，尤其是老年人要有意識地進行精神調養，保持神清氣和、心情愉快的狀態，切忌大悲大喜，以免傷心、傷身、傷神。</p>
