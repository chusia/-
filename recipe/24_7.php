	<h3>習俗</h3>
<p>在立夏的這一天，古代帝王要率文武百官到京城南郊去迎夏，舉行迎夏儀式。君臣一律穿朱色禮服，配朱色玉佩，連馬匹、車旗都要朱紅色的，以表達對豐收的企求和美好的願望。宮廷裡&ldquo;立夏日啟冰，賜文武大臣&rdquo;。冰是上年冬天貯藏的，由皇帝賜給百官。<br />
在民間，立夏日人們則喝冷飲來消暑。 立夏日，江南水鄉有烹食嫩蠶豆的習俗。 有的地方還有立夏日稱人的習俗。</p>
