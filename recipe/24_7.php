	<h3>由來</h3>
<p>穀雨是春季的最後一個節氣，這時田中的秧苗初插、作物新種，最需要雨水的滋潤，所以說&ldquo;春雨貴如油&rdquo;。穀雨是&ldquo;雨生百穀&rdquo;的意思，每年4月20日或21日太陽到達黃經30&deg;時為穀雨。<br />
《月令七十二候集解》中說，&ldquo;三月中，自雨水後，土膏脈動，今又雨其穀于水也&hellip;&hellip;蓋穀以此時播種，自下而上也&rdquo;，故此得名。<br />
穀雨時節，南方地區&ldquo;楊花落盡子規啼&rdquo;，柳絮飛落，杜鵑夜啼，牡丹吐蕊，櫻桃紅熟，自然景物告示人們：時至暮春了。<br />
關於穀雨節的來歷，據《淮南子》記載，倉頡造字，是一件驚天動地的大事，黃帝于春末夏初發佈詔令，宣佈倉頡造字成功，並號召天下臣民共習之。這一天，下了一場不平常的雨，落下無數的谷米，後人因此把這天定名穀雨，成為二十四節氣中的一個。</p>
