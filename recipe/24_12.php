	<h3>養生</h3>
<p>中醫認為：大暑時節，梅雨剛過，人體容易為暑、濕、邪所侵，故而需吃熱性食物以驅除暑濕，恰合&ldquo;冬病夏治&rdquo;、&ldquo;寒病夏治&rdquo;之醫理。例如三伏天去拔火罐、貼&ldquo;伏帖&rdquo;最容易去除體內的寒，說的就是這個道理。<br />
大暑期間應該多吃絲瓜、西蘭花和茄子等當季蔬菜。大暑天氣酷熱，出汗多，脾胃活動相對較差。這時人會感覺比較累和食欲不振。而淮山有補脾健胃，益氣補腎作用。多吃淮山一類益氣養陰的食品，可以促進消化，改善腰膝酸軟，使人感到精力旺<br />
如果你是高血壓或糖尿病患者，那麼吃南瓜就最好不過。南瓜富含維生素、蛋白質和多種氨基酸，而且以碳水化合物為主，脂肪含量很低，多吃有助於降低血糖和血脂。另外，南瓜還能排毒養顏，愛美的女士當然不能錯過。俗語說&ldquo;冬吃蘿蔔夏吃薑&rdquo;。吃薑有助於驅除體內寒氣，大家可以嘗試一下子薑炒牛肉、子薑炒木耳等菜式。但吃薑的時間也有講究，最好不要在晚上吃。冬補三九，夏補三伏。家禽肉的營養成分主要是蛋白質，其次是脂肪、微生物和礦物質等，相對於家畜肉而言，是低脂肪高蛋白的食物，其蛋白質也屬於優質蛋白。雞、鴨、鴿子等家禽都是大暑進補的上選。</p>
