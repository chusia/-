<h3>介紹</h3>
<p>大暑，二十四節氣之一。在每年的7月23日或24日，太陽到達黃經120&deg;。《月令七十二候集解》：&ldquo;六月中，&hellip;&hellip;暑，熱也，就熱之中分為大小，月初為小，月中為大，今則熱氣猶大也。&rdquo;</p>
<h3>由來</h3>
<p>我國古代將大暑分為三候：&ldquo;一候腐草為螢；二候土潤溽暑；三候大雨時行。&rdquo;世上螢火蟲約有二千多種，分水生與陸生兩種，陸生的螢火蟲產卵於枯草上，大暑時，螢火蟲卵化而出，所以古人認為螢火蟲是腐草變成的；第二候是說天氣開始變得悶熱，土地也很潮濕；第三候是說時常有大的雷雨會出現，這大雨使暑濕減弱，天氣開始向立秋過渡。</p>
