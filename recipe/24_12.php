	<h3>習俗</h3>
<p>大暑節氣的民俗主要體現在吃的方面，這一時期民間飲食習俗大致分為兩種：一種是吃涼性食物消暑。湘東南地區則流行一句廣東諺語：六月大暑吃仙草，活如神仙不會老。仙草即&ldquo;涼粉草&rdquo;，有神奇的消暑功效，用它做成的&ldquo;燒仙草&rdquo;在外觀、口味、療效上均與龜苓膏類似。臺灣則有大暑吃鳳梨之俗，乃是因為這個時節的鳳梨最好吃。<br />
與此相反的是，有些地方的人們大暑時節偏要吃熱性食物。如福建莆田人要吃荔枝、羊肉和米糟來&ldquo;過大暑&rdquo;。湘中、湘北素有一傳統的進補方法，就是大暑吃童子雞。湘東南還有大暑吃薑的風俗，&ldquo;冬吃蘿蔔夏吃薑，不需醫生開藥方&rdquo;。</p>

