<h3>習俗</h3>
<p>六月初六日為&ldquo;天貺節&rdquo;。據史書記載，此節始于宋代哲宗元符四年。&ldquo;貺&rdquo;即&ldquo;賜&rdquo;，即天賜之節。這是宋代皇帝在伏天向臣屬賜&ldquo;冰麨&rdquo;和&ldquo;炒麵&rdquo;之因，故稱天貺節。<br />
頭伏蘿蔔二伏菜，三伏還能種蕎麥；頭伏餃子，二伏面，三伏烙餅攤雞蛋。小暑頭伏吃餃子是傳統習俗，伏日人們食欲不振，往往比常日消瘦，俗謂之苦夏，而餃子在傳統習俗裡正是開胃解饞的食物山東有的地方吃生黃瓜和煮雞蛋來治苦夏，入伏的早晨吃雞蛋，不吃別的食物。<br />
徐州人入伏吃羊肉，稱為吃伏羊這種習俗可上溯到堯舜時期，在民間有彭城伏羊一碗湯，不用神醫開藥方之說法。徐州人對吃伏羊的喜愛莫過於當地民謠：六月六接姑娘，新麥餅羊肉湯。伏日吃面習俗至少三國時期就已開始了。</p>

