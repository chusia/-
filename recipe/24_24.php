<h3>習俗</h3>
<p>按大寒節氣變化備農事<br />
大寒節氣裡，各地農活依舊很少。北方地區老百姓多忙於積肥堆肥，為開春作準備；或者加強牲畜的防寒防凍。南方地區則仍加強小麥及其他作物的田間管理。廣東嶺南地區有大寒聯合捉田鼠的習俗。因為這時作物已收割完畢，平時看不到的田鼠窩多顯露出來，大寒也成為嶺南當地集中消滅田鼠的重要時機。如&ldquo;大寒天若雨，正二三月雨水多&rdquo;（廣西）、&ldquo;大寒見三白，農民衣食足&rdquo;（江西）、&ldquo;大寒不寒，人馬不安&rdquo;（福建）、&ldquo;大寒白雪定豐年&rdquo;（貴州）、&ldquo;大寒無風伏乾旱&rdquo;。 <br />
小寒之後過15天就是大寒，也是全年二十四節氣中的最後一個節氣。這時節，人們開始忙著除舊飾新，醃制年肴，準備年貨，因為中國人最重要的節日&mdash;&mdash;春節就要到了。其間還有一個對於北方人非常重要的日子&mdash;&mdash;臘八，即陰曆十二月初八。在這一天，人們用五穀雜糧加上花生、栗子、紅棗、蓮子等熬成一鍋香甜美味的臘八粥，是人們過年中不可或缺的一道主食。</p>
