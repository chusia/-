<h3>養生</h3>
<p>冬三月是生機潛伏、萬物蟄藏的時令，此時人體的陰陽消長代謝也處於相當緩慢的時候，所以此時應該早睡晚起，不要輕易擾動陽氣，凡事不要過度操勞，要使神志深藏於內，避免急躁發怒。大寒是冬季六節氣之一，此時天氣寒冷已極，故名大寒。大寒的養生，要著眼於&ldquo;藏&rdquo;。意思是說，人們在此期間要控制自己的精神活動，保持精神安靜，把神藏於內不要暴露於外。這樣才有利於安度冬季。<br />
總之，大寒是二十四節氣之尾，也是冬季即將結束之季，隱隱中已可感受到大地回春的跡象。</p>
