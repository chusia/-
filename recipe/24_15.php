	<h3>由來</h3>
<p>白露是九月的頭一個節氣。露是由於溫度降低，水汽在地面或近地物體上凝結而成的水珠。所以，白露實際上是表徵天氣已經轉涼。這時，人們就會明顯地感覺到炎熱的夏天已過，而涼爽的秋天已經到來了。因為白天的溫度雖然仍達三十幾度，可是夜晚之後，就下降到二十幾度，兩者之間的溫度差達十多度。<br />
陽氣是在夏至達到頂點，物極必反，陰氣也在此時興起。到了白露，陰氣逐漸加重，清晨的露水隨之日益加厚，凝結成一層白白的水滴，所以就稱之為白露。<br />
俗語雲：&ldquo;處暑十八盆，白露勿露身。&rdquo;這兩句話的意思是說，處暑仍熱，每天須用一盆水洗澡，過了十八天，到了白露，就不要赤膊裸體了，以免著涼。還有句俗話：&ldquo;白露白迷迷，秋分稻秀齊。&rdquo;意思是說，白露前後若有露，則晚稻將有好收成。</p>

