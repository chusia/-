<h3>介紹</h3>
<p>白露是24節氣之一，此時氣溫開始下降，天氣轉涼，早晨草木上有了露水。每年西曆的9月7日前後是白露。我國古代將白露分為三候：&ldquo;一候鴻雁來；二候玄鳥歸；三候群鳥養羞。&rdquo;說此節氣正是鴻雁與燕子等候鳥南飛避寒，百鳥開始貯存乾果糧食以備過冬。可見白露實際上是天氣轉涼的象徵。<br />
每年9月8日前後太陽到達黃經165度時，交&ldquo;白露&rdquo;節氣。&ldquo;白露&rdquo;是反映自然界氣溫變化的節令。露是&ldquo;白露&rdquo;節氣後特有的一種自然現象。此時的天氣，正如《禮記》中所雲的：&ldquo;涼風至，白露降，寒蟬鳴。&rdquo;據《月令七十二候集解》對&ldquo;白露&rdquo;的詮釋&mdash;&mdash;&ldquo;水土濕氣凝而為露，秋屬金，金色白，白者露之色，而氣始寒也&rdquo;。古人在《孝緯經》中也雲：&ldquo;處暑後十五日為白露&rdquo;，陰氣漸重，露凝而白也。&rdquo;</p>
