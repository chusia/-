<h3>養生</h3>
<p>白露是典型的秋天節氣，古語說：&ldquo;白露節氣勿露身，早晚要叮嚀。&rdquo;意在提醒人們此時白天雖然溫和，但早晚已涼，打赤脯容易著涼。<br />
白露時節，支氣管哮喘發病率很高，因此要做好預防工作。此節氣的養生重點是加強身體鍛煉，注意早晚不要受涼，對過敏性疾病積極預防。<br />
白露時節要防止鼻腔疾病、哮喘病和支氣管病的發生。特別是因體質過敏而引發上述疾病的患者，在飲食調節上更要慎重，平時要少吃或不吃魚蝦海腥、生冷炙燴醃菜和辛辣酸咸甘肥的食物。<br />
在白露一節氣中要預防秋燥，燥邪傷人，容易耗人津液，而出現口幹、唇幹、鼻幹、咽幹及大便幹結、皮膚乾裂等症狀。預防秋燥的方法很多，可適當地多服一些富含維生素的食品，也可選用一些宣肺化痰、滋陰益氣的中藥，如人參、沙參等，對緩解秋燥有良效。</p>
