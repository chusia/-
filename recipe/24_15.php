<h3>習俗</h3>
<p>福州有個傳統叫作&ldquo;白露必吃龍眼&rdquo;的說法。在白露這一天吃龍眼有大補身體的奇效，在這一天吃一顆龍眼相當於吃一隻雞那麼補。<br />
浙江溫州等地有過白露節的習俗。蒼南、平陽等地民間，人們於此日採集&ldquo;十樣白&rdquo;（也有&ldquo;三樣白&rdquo;的說法），以煨烏骨白毛雞（或鴨子），據說食後可滋補身體，去風氣（關節炎）。民間認為白露吃番薯可使全年吃番薯絲和番薯絲飯後，不會發胃酸，故舊時農家在白露節以吃番薯為習。<br />
老南京人都十分青睞&ldquo;白露茶&rdquo;，此時的茶樹經過夏季的酷熱，白露前後正是它生長的極好時期。<br />
南籍和浙江籍的老南京中還有自釀白露米酒的習俗，白露酒用糯米、高粱等五穀釀成，略帶甜味，故稱&ldquo;白露米酒&rdquo;。白露時節也是太湖人祭禹王的日子。</p>

