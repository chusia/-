	<h3>習俗</h3>
<p>立春亦稱&ldquo;打春&rdquo;、&ldquo;咬春&rdquo;，又叫&ldquo;報春&rdquo;。這個節令與眾多節令一樣有眾多民俗，有迎春行春的慶賀祭典與活動，有打春的&ldquo;打牛&rdquo;和咬春吃春餅、春盤、咬蘿蔔之習俗等。</p>
<p>從歷史文獻記載來看，我國歷朝歷代的迎春儀式隆重而浩大。此時的立春已經超過了農曆２４節氣只標示節令與氣候的功能，而是已經被當成一個節日了，因此在歷史上便演化出了許多圍繞立春而舉辦的活動及民俗，如&ldquo;春娃&rdquo;、&ldquo;春鞭&rdquo;、&ldquo;春捲&rdquo;、&ldquo;打春&rdquo;、&ldquo;春酒&rdquo;、&ldquo;春牛&rdquo;等等。</p>
<p>咬春是指立春日吃春盤、吃春餅、吃春捲、嚼蘿蔔之俗，一個&ldquo;咬&rdquo;字道出節令的眾多食俗。春盤春餅是用蔬菜、水果、餅餌等裝盤饋送親友或自食，稱為春盤。</p>
