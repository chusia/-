	<h3>養生</h3>
<p>飲食調養方面要考慮春季陽氣初生，宜食辛甘發散之品，不宜食酸收之味。《素問藏氣法時論》說：&ldquo;肝主春，&hellip;&hellip;肝苦急，急食甘以緩之，&hellip;&hellip;肝欲散，急食辛以散之，用辛補之，酸瀉之&rdquo;。過了立春就意味著春天要到了，萬物生髮，一派生機勃勃。</p>
<p>春季養生要順應春天陽氣生髮，萬物始生的特點，注意保護陽氣，著眼於一個&ldquo;生&rdquo;字。按自然界屬性，春屬木，與肝相應。肝的生理特點主疏泄，在志為怒，惡抑鬱而喜調達。有目的地選擇一些柔肝養肝、疏肝力理氣的草藥和食品，草藥如枸杞、郁金、丹參、元胡等，食品選擇辛溫發散的大棗、豆豉、蔥、香菜、花生等靈活地進行配方選膳。</p>
<p>在春季精神養生方面，要力戒暴怒，更忌情懷憂鬱，做到心胸開闊，樂觀向上，保持心境愉悅的好心態。同時要充分利用、珍惜春季大自然&ldquo;發陳&rdquo;之時，借陽氣上升，萬物萌生，人體新陳代謝旺盛之機，通過適當的調攝，使春陽之氣得以宣達，代謝機能得以正常運行。</p>
